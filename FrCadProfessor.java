    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Professor p = new Professor();
        p.setNome(edtNome.getText());
        p.setCpf(edtCPF.getText());
        p.setSexo(edtSexo.getText().charAt(0));
        String idade = edtIdade.getText();
        if (!idade.isEmpty()) {/*o mesmo que if(idade.equals(""))*/

            int aux = Integer.parseInt(idade);
            p.setIdade(aux);
        } else {
            JOptionPane.showMessageDialog(this, "Campo idade obrigat√≥rio");
        }
        edtResultado.setText(p.imprimirProfessorString());
        resetFields(false);
    }//GEN-LAST:event_btnSalvarActionPerformed

